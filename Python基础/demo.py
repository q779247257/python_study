# -*- coding: utf-8 -*-

a = 123 # a是一个int类型的数字
print(a)
a = 'ANC' # a变为一个字符串
print(a)

# 这种变量本身类型不固定的语言称为动态语言，与之对应的是静态语言。静态语言在定义变量时必须指定变量类型，如果复制的时候类型不匹配就会报错，
# 例如java和C##


